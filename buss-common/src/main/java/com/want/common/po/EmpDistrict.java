package com.want.common.po;
import java.io.Serializable;
import java.util.Date;
public class EmpDistrict implements Serializable {
	private static final long serialVersionUID = -3329289164037846820L;
	private String empId        ; //业代编码    
	private String empName      ; //业代名称    
	private String districtId   ; //大区编码       
	private String districtName ; //大区名称         
	private String status       ; //状态 1 有效：0 无效    
	private String createUser   ; //创建人       
	private Date createDate     ; //创建时间  
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public String getDistrictId() {
		return districtId;
	}
	public void setDistrictId(String districtId) {
		this.districtId = districtId;
	}
	public String getDistrictName() {
		return districtName;
	}
	public void setDistrictName(String districtName) {
		this.districtName = districtName;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getCreateUser() {
		return createUser;
	}
	public void setCreateUser(String createUser) {
		this.createUser = createUser;
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
}
