package com.want.common.po;

import java.io.Serializable;

public class Product implements Serializable {
	/**
	 * 序列号
	 */
	private static final long serialVersionUID = 1L;
	/**
	 * 品项编码
	 */
	private String id;
	/**
	 * 品项名称
	 */
	private String name;
	/**
	 * 产品规格
	 */
	private String specTaste;
	/**
	 * 物料类型编码
	 */
	private String materialType;
	/**
	 * 单位
	 */
	private String baseUnit;
	/**
	 * 五层产品层级编码
	 */
	private String lv5Id;
	/**
	 * 更新日期
	 */
	private String updateDate;

    public Product() {
    }

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSpecTaste() {
		return specTaste;
	}

	public void setSpecTaste(String specTaste) {
		this.specTaste = specTaste;
	}

	public String getMaterialType() {
		return materialType;
	}

	public void setMaterialType(String materialType) {
		this.materialType = materialType;
	}

	public String getBaseUnit() {
		return baseUnit;
	}

	public void setBaseUnit(String baseUnit) {
		this.baseUnit = baseUnit;
	}

	public String getLv5Id() {
		return lv5Id;
	}

	public void setLv5Id(String lv5Id) {
		this.lv5Id = lv5Id;
	}

	public String getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}

}